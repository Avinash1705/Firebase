package com.example.searchkaro;

public class newItemData {
    int img;
    String detail;

    public newItemData(int img, String detail) {
        this.img = img;
        this.detail = detail;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }
}
